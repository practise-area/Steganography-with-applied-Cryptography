    private String convertToBits(int value)
    {
        String bitStream=bytestring;
        bitStream+=Integer.toBinaryString(value);
        bitStream=bitStream.substring(bitStream.length()-8,bitStream.length());  //return last 8 bits 
        return(bitStream);
    }
