    private StringBuilder makeZigZag(StringBuilder data)
    {
        //zigzag rule apply on the given data so it gets scrambled here
        String pdata=data.toString();
        String temp="";
        char []idata=pdata.toCharArray();
        String bitzigzag="";
        int lr=1;
        for(int i=0;i<idata.length;i++)
        {
                 if (lr == -1)
                {
                    temp = "";
                    for (int j = bitzigzag.length() - 1; j >= 0; j--)
                        temp += bitzigzag.toCharArray()[j];
                    bitzigzag = temp;
                    bitzigzag += idata[i];
                    temp = "";
                    for (int j = bitzigzag.length() - 1; j >= 0; j--)
                        temp += bitzigzag.toCharArray()[j];
                    bitzigzag = temp;
                    lr = 1;
                }
                else
                {
                    bitzigzag += idata[i];
                    lr = -1;
                }
        }
        return(new StringBuilder(bitzigzag));
    }
    
  
