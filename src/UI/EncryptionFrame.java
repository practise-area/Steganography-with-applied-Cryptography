    void CreatePartitions(int N)
    {
     
        //jTextArea1.setText("Creating Partitons Data For " + N + "  Partitions" );            
        double psize=Math.ceil((double)partiondata.length()/totalpartitions);
         partitionsize=(int)psize;
         JOptionPane.showMessageDialog(this,"Partitions " + totalpartitions+  " Size of each partition " + partitionsize);
         String data;
         
         int start;
        int bytepos=0;
         for(start=1;start<=N;start++)
         {
                data=partiondata.substring(((start-1)*partitionsize),(start*partitionsize));
                System.out.println("Partition " + start);
                al.add(data);
                bytepos+=partitionsize;
                if(bytepos+partitionsize>=partiondata.length())
                    break;
         }
         
         if(bytepos+partitionsize<partiondata.length())
         {
         while((bytepos+partitionsize)<partiondata.length())
         {
             data=partiondata.substring(bytepos,bytepos+partitionsize);
             al.add(data.trim());
             bytepos+=partitionsize;
         }
         data=partiondata.substring(bytepos);
  //       JOptionPane.showMessageDialog(this,"Last Partition size is " + data);
         al.add(data.trim());
         }
         else
         {
         data=partiondata.substring(bytepos);
         if(data.length()>0)
         {
  //       JOptionPane.showMessageDialog(this,"Last Partition size is " + data);
         al.add(data.trim());
         }
         }
         jTextArea1.setText("");
         //show partitions here
         for(int i=0;i<al.size();i++)
         {
           jTextArea1.append("\r\nPartition-" + (i+1)  +"\r\n");                 
           jTextArea1.append(al.get(i).toString().trim() + "\r\n");
           jTextArea1.append("<--------------->\r\n");
         }
    }
